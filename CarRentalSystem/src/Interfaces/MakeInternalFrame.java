    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String check = makebox.getText();
        int x = check.compareTo("");
        if(x==0){
            JOptionPane.showMessageDialog(null, "Cannot Perform action: Should enter a Make before Adding");
        }
        else{
            
            String make = makebox.getText().toUpperCase();
            Make mk = new Make(make);
            try {
                mk.AddMake();
                LoaddTable();
            }
            catch (SQLException ex) {
                Logger.getLogger(AdminsInternalFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        makebox.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

